var numBottles: Int = 99

while numBottles > 0 {
  print("\(numBottles) of milk on the wall, \(numBottles) bottles of milk!") 
  print("You take one down pass it around...") 
  numBottles -= 1
  print("\(numBottles) bottles of milk on the wall!")
}

